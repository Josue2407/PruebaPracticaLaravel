     * Store a newly created resource in storage.
     *
     * @param  \Illuminate\Http\Request  $request
     * @return \Illuminate\Http\Response
     */
    public function store(Request $request)
    {
        $registro = new Datos();

        $registro->nombre = $request->get('nombre');
        $registro->apellido = $request->get('apellido');
        $registro->email = $request->get('email');
        $registro->habilidades = $request->get('habilidades');
        $registro->edad = $request->get('edad');

        $registro->save();

        return view('datos.index');
    }

